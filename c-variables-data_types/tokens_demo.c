#include <stdio.h>
int main() {
int age = 25;// int=keyword age= identifier '=' = operator 25 = literal '; ' = punctuation
float grade = 15.7;// float = keyword grade = identifier 15.7 = literal '= ' = operator '; ' = punctuation
char stage = 'A';// char = keyword stage = identifier  '= ' = operator  'A' =  literal ';' = punctuation
printf ( "Integer:%d\n",age);
printf ("Float:%f\n",grade);
printf ( "Char:%c\n",stage);
return 0;
}
